public class operators {
    public static void main(String[] args) {
        int a=20;
        a++;
        System.out.println("value="+a);
        a--;
        System.out.println("value="+a);
        ++a;
        System.out.println("value="+a);
        --a;
        System.out.println("value="+a);
    }
}
